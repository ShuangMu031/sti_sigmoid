# =========================
# 通用数值
# =========================
EPS = 1e-6
# 数值稳定用极小量
# 作用：防止除零、log(0) 等数值异常


# =========================
# Canny 边缘检测（edge_detect.py）
# =========================
CANNY_LOW_THRESH = 0.1
# Canny 低阈值（0~1 归一化）
# ↓ 调小：检测到更多弱边缘（噪声↑）
# ↑ 调大：只保留强边缘（结构更干净）

CANNY_HIGH_THRESH = 0.3
# Canny 高阈值（0~1 归一化）
# 通常是 LOW 的 2~3 倍，用于双阈值连接

CANNY_SIGMA = 1.2
# 高斯平滑 sigma
# ↑：边缘更平滑但细节减少
# ↓：边缘更敏感但噪声增加


# =========================
# 显著性 / 主体检测相关（object_height.py）
# =========================
SALiency_THRESH = 0.3
# 显著性阈值
# 用于区分“主体区域”和“背景区域”
# ↑：主体更保守（只保留最显著部分）
# ↓：主体区域扩大，可能引入背景

MIN_NON_EDGE_AREA = 400
# 非边缘区域的最小连通面积
# 作用：过滤掉小块噪声区域
# ↑：更强的噪声抑制
# ↓：保留更多细碎结构


# =========================
# 特征匹配 / 单应性估计（register_texture.py）
# =========================

HIST_SHIFT_BIN_RATIO = 0.05
# Δx / Δy 直方图过滤的 bin 宽度比例（相对于图像尺寸）
# 用于在 RANSAC 前约束匹配点的整体位移一致性，值越小约束越严格

SIFT_EDGE_THRESH = 500
# SIFT 的 edgeThreshold 参数
# 控制是否保留位于边缘上的特征点，值越大越允许边缘特征（适合图像拼接）

SIFT_RATIO_THRESH = 0.75
# Lowe ratio test 阈值
# ↓（如 0.6）：匹配更严格，内点更少但更准
# ↑（如 0.8）：匹配更多，但错误匹配风险增加

RANSAC_REPROJ_THRESH = 1.5
# RANSAC 重投影误差阈值（像素）
# ↓：几何约束更严格，容易失败
# ↑：更宽松，可能引入错误 H


# =========================
# Graph Cut 拼接缝（graph_cut.py）
# =========================
GC_SALIENCY_WEIGHT = 3.0
# 显著性权重
# 控制 seam 是否避开显著区域（如人、建筑）
# ↑：更不容易切过主体
# ↓：seam 更自由，但可能穿过重要区域

GC_OBJECT_WEIGHT = 4.0
# 主体高度 / 结构先验权重
# 作用：惩罚 seam 穿过竖直主体结构
# ↑：更偏向沿天空/地面走
# ↓：结构约束减弱

GC_EDGE_PENALTY = 5.0
# 边缘惩罚系数
# seam 穿越强边缘（梯度大处）的惩罚
# ↑：更沿着平坦区域走
# ↓：允许跨越边缘


# =========================
# Seam 软过渡与 Poisson
# =========================
SEAM_SOFT_BAND = 20
# Graph Cut 后的软过渡带宽度（像素）
# ↑：过渡更平滑，但可能产生重影
# ↓：过渡更锐利，可能看到 seam

SEAM_POISSON_BAND = 3
# 用于 Poisson 融合的窄 seam 带宽
# 只修 seam 附近，避免全图颜色漂移


# =========================
# 金字塔融合（pyramid_blend.py）
# =========================
PYRAMID_LEVELS = 6
# Laplacian 金字塔层数
# ↑：低频融合更好，颜色过渡自然
# ↓：更保留原图细节，但可能有接缝


# =========================
# 重叠区后处理过渡带（main.py）
# =========================
TRANSITION_KERNEL_SIZE = 9
# 过渡带形态学核尺寸（必须是奇数）
# 控制 overlap 外围的安全融合宽度
# ↑：过渡区域更宽
# ↓：更接近硬切换


# =========================
# 曝光补偿（main.py）
# =========================
EXPOSURE_MIN_PIXELS = 100
# 用于计算曝光增益的最小像素数
# 防止 overlap 太小导致曝光不稳定

EXPOSURE_EPS = 1e-6
# 曝光补偿中的数值稳定项

EXPOSURE_MIN_GAIN = 0.5
# 曝光增益的最小限制值
# 避免图像过暗

EXPOSURE_MAX_GAIN = 2.0
# 曝光增益的最大限制值
# 避免图像过亮

EXPOSURE_TRANSITION_KERNEL_SIZE = 23
# 曝光调整过渡带的形态学核尺寸（必须是奇数）
# 控制曝光调整的平滑过渡范围
# ↑：过渡更平滑，范围更广
# ↓：过渡更锐利，范围更小

EXPOSURE_ADJUSTMENT_STRENGTH = 0.95
# 曝光调整强度因子（0.0-1.0）
# 控制两个图像之间的曝光平衡程度
# 1.0：完全按照计算的增益调整
# 0.5：只应用一半的调整强度，保持一定的原始特性
# 增加强度使色调调整更明显



